package studentDetails;

public class Address 

{
	String house_name;
	String post_office;
	String district;
	int zip_code;
	String Country;
	
	public Address(String house_name,String post_office,String district,int Zip_code,String Country)
	{
		
		this.house_name=house_name;
		this.post_office=post_office;
		this.district=district;
		this.zip_code=Zip_code;
		this.Country=Country;
		
	}

}
